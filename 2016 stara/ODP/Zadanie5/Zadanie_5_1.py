def sasiedzi(plansza, x, y):
    s = 0
    if plansza[(y - 1) % 12][(x - 1) % 20] == 'X':
        s += 1
    if plansza[(y - 1) % 12][x % 20] == 'X':
        s += 1
    if plansza[(y - 1) % 12][(x + 1) % 20] == 'X':
        s += 1
    if plansza[y % 12][(x - 1) % 20] == 'X':
        s += 1
    if plansza[y % 12][(x + 1) % 20] == 'X':
        s += 1
    if plansza[(y + 1) % 12][(x - 1) % 20] == 'X':
        s += 1
    if plansza[(y + 1) % 12][x % 20] == 'X':
        s += 1
    if plansza[(y + 1) % 12][(x + 1) % 20] == 'X':
        s += 1

    return s


